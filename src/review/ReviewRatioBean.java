package review;

public class ReviewRatioBean {
	int like;
	int dislike;
	public ReviewRatioBean() {
		super();
	}
	public int getLike() {
		return like;
	}
	public void setLike(int like) {
		this.like = like;
	}
	public int getDislike() {
		return dislike;
	}
	public void setDislike(int dislike) {
		this.dislike = dislike;
	}
	
	
}
